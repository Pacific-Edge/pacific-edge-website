  React.ElementRef<typeof TabsPrimitive.Trigger>,
  React.ComponentPropsWithoutRef<typeof TabsPrimitive.Trigger>
>(({ className, ...props }, ref) => (
  <TabsPrimitive.Trigger
    ref={ref}
    className={cn(
      "inline-flex items-center justify-center whitespace-nowrap rounded-pill px-3 py-1.5 font-ui text-sm font-medium outline-offset-2 transition-all hover:text-midnight-900/70 focus-visible:outline focus-visible:outline-2 focus-visible:outline-midnight-900/30 disabled:pointer-events-none disabled:opacity-50 data-[state=active]:bg-white-50 data-[state=active]:text-midnight-900 data-[state=active]:shadow-soft",
      className,
    )}
    {...props}
  />
))
TabsTrigger.displayName = TabsPrimitive.Trigger.displayName

